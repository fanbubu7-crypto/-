def get_github_headers():
    """获取GitHub请求头"""
    headers = SCRAPER_CONFIG['github']['headers'].copy()
    if GITHUB_TOKEN:
        headers['Authorization'] = f'token {GITHUB_TOKEN}'
    return headers


def get_filter_config():
    """获取筛选配置"""
    return FILTER_CONFIG.copy()


def get_ranking_weights():
    """获取排名权重"""
    return RANKING_WEIGHTS.copy()


# 验证权重总和为1
_weights_sum = sum(RANKING_WEIGHTS.values())
if abs(_weights_sum - 1.0) > 0.001:
    raise ValueError(f"排名权重总和必须为1.0，当前为 {_weights_sum}")
